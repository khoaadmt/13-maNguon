    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // Sắp xếp danh sách theo họ tên
        Comparator<CanBo> comp = new Comparator<>() {
            @Override
            public int compare(QuanLyCanBo.CanBo o1, QuanLyCanBo.CanBo o2) {
                return o1.getTenCB().compareToIgnoreCase(o2.getTenCB());
            }
        };
        Collections.sort(dscb, comp);
        loadTableCanBo();
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // Tìm kiếm cán bộ theo tên
        String dscbtk = "";
        for (CanBo cb: dscb ) {
            if (cb.getTenCB().compareTo(txtTenCanBo.getText())==0) {
                dscbtk += cb.toString() + "\n";
            }
        }
        JOptionPane.showMessageDialog(this, dscbtk,
                "Những cán bộ tìm được", WIDTH);
    }//GEN-LAST:event_jButton5ActionPerformed

