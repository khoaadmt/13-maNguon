    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //Sửa thông tin cán bộ
        dongChon = TableCanBo.getSelectedRow();
        if (dongChon!=-1) {
            CanBo cbNew = new CanBo();
            try {
            if (txtMaCanBo.getText().trim().equals("")) {
                throw new Exception("Mã cán bộ không được để trống!");
            }
            if (dscb.contains(canbo = new CanBo(txtMaCanBo.getText(), txtTenCanBo.getText()))) {
                throw new Exception("Cán bộ đã tồn tại!");
            }
            if (txtTenCanBo.getText().trim().equals("")) {
                throw new Exception("Tên cán bộ không được để trống!");
            }
            
            cbNew.setMaCB(txtMaCanBo.getText()+"");
            cbNew.setTenCB(txtTenCanBo.getText()+"");
            dscb.set(dongChon, cbNew);//vị trí là dòng chọn
            //dữ liệu mới là từ giao diện cập nhật
            loadTableCanBo();
        } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), 
                       "Cảnh báo", WIDTH);
        }
        } else {
            JOptionPane.showMessageDialog(this,
                    "Chưa chọn dòng sửa","Thông báo", WIDTH);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    //Thêm cán bộ
        canbo = new CanBo(txtMaCanBo.getText(), txtTenCanBo.getText());
        try {
            if (txtMaCanBo.getText().trim().equals("")) {
                throw new Exception("Mã cán bộ không được để trống!");
            }
            if (dscb.contains(canbo)) {
                throw new Exception("Mã cán bộ đã tồn tại!");
            }
            if (txtTenCanBo.getText().trim().equals("")) {
                throw new Exception("Tên cán bộ không được để trống!");
            }
            dscb.add(canbo);
            loadTableCanBo();
        } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), 
                       "Cảnh báo", WIDTH);
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

